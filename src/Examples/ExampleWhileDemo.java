package Examples;
// цикл while
public class ExampleWhileDemo {
    public static void main(String[] args) {
        char ch;

        // вывести буквы английского алфавита, используя цикл while
        ch = 'a';
        while (ch <= 'z') {
            System.out.println(ch);
            ch++;
        }
    }
}
