package Exercises;

public class CharArithDemo {
    public static void main(String[] args) {
        char ch;

        ch = 'X';
        System.out.println("ch содердит " + ch);

        ch++; //инкрементировать (увеличение значения)
              //переменную ch
        System.out.println("теперь ch содержит " + ch);

        ch = 90;
        System.out.println("теперь ch содержит " + ch);
    }
}
